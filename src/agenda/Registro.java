    public void Buscar(String pos){
       for(int cont=0;cont<usuarios.length;cont++){
           if(usuarios[cont].equalsIgnoreCase(pos)){
               txtUsuario.setText(usuarios[cont]);
               txtNumero.setText(telefono[cont]);
               break;
           }
       }
    }
    

