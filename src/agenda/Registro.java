    private void btnregistrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnregistrarMouseClicked
        if (registroTotales>10){
          JOptionPane.showMessageDialog(null, "USTED A ALCANZADO LA MAXIMA CANTIDAD DE REGISTROS");
        }
        else{
            registroTotales++;
            registroActual=registroTotales;
            usuarios[registroActual]=txtUsuario.getText();
            telefono[registroActual]=txtNumero.getText();
            txtEditable(false);
            txtbuscar.setEnabled(true);
            btnNuevo.setEnabled(true);
            btnregistrar.setEnabled(false);
            btnbuscar.setEnabled(true);
        }
    }//GEN-LAST:event_btnregistrarMouseClicked

    private void btnAtrasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtrasActionPerformed
        if (registroActual>0){
            registroActual--;
            MostrarContactos(registroActual);
        }
        else{
            registroActual=registroTotales;
            MostrarContactos(registroActual);
        }
    }//GEN-LAST:event_btnAtrasActionPerformed

